async function updateProductDescriptions() {
  try {
    console.log('Обновляем описания товаров...');
    console.log('===============================');

    for (const [slug, description] of Object.entries(productDescriptions)) {
      const updated = await prisma.product.update({
        where: { slug },
        data: { description },
        select: { name: true, slug: true }
      });

      console.log(`✅ Обновлено: ${updated.name} (${updated.slug})`);
      console.log(`   Длина описания: ${description.length} символов`);
      console.log('');
    }

    console.log('🎉 Все описания успешно обновлены!');

  } catch (error) {
    console.error('❌ Ошибка при обновлении описаний:', error);
  } finally {
    await prisma.$disconnect();
  }
}

updateProductDescriptions();

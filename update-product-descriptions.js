async function run() {
  try {
    for (const [slug, description] of Object.entries(DESCRIPTIONS)) {
      const product = await prisma.product.findUnique({ where: { slug } });
      if (!product) {
        console.log(`Товар с slug "${slug}" не найден, пропускаем.`);
        continue;
      }
      const updated = await prisma.product.update({
        where: { slug },
        data: { description }
      });
      console.log(`Описание обновлено: ${updated.name} (${slug})`);
    }
  } catch (err) {
    console.error('Ошибка при обновлении описаний:', err);
  } finally {
    await prisma.$disconnect();
  }
}

run();
